package FlowerBoquet;
public class Flower {

	int numberOfFlowers;
	int costOfFlower;

	public Flower( int numberOfFlowers, int costOfFlower) {
		super();
		this.numberOfFlowers = numberOfFlowers;
		this.costOfFlower = costOfFlower;
	}
	public int getNumberOfFlowers() {
		return numberOfFlowers;
	}

	public int getCostOfFlower() {
		return costOfFlower;
	}



}
